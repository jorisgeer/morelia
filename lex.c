static int lex(struct fnaminf *mf,const unsigned char * restrict sp,ub4 slen,struct lexsyn *lsp,ub8 T0)
{
  int rv = 0;
  ub4 len;

  enum Slitctl slitctl=0,fstrctl=0;
  bool litbin = 1;
  bool litfpi=0;

  // tokens
  enum Token tk=0;
  enum Bltin blt;
  enum Dunder dun;
  ub4 tkcnt=0;
  void *tkbas=nil;
  ub1 *tks=nil;
  ub1 *atrs=nil;
  ub8 *bits=nil;
  ub2 *dfps=nil;
  struct mempart tkpart[4];

  ub4 tkgrps[Tkgrp];
  ub1 grp;

  // str lits
  ub1 *slitpool=nil;
  ub4 slitx=0;
  ub4 idxpos=0;

  // indent
  static ub2 dentst[Dent];
  ub2 dent;
  ub2 dentlvl=0;

  // braces
  ub4 bolvls[Depth];
  ub1 bolvlc[Depth];
  ub2 bolvl=0;

// line/col/dent
  ub4 l=0;
  ub4 nlcol=0;
  ub4 *lntab=nil;
  ub4 lncnt;
  bool hasdent;

  ub4 cnt;

  ub8 T1=T0;

  timeit(&T1,nil);

  ub1 c,prvc1,prvc2;
  ub4 dn = 0,bn = 0;
  ub4 n = 0, prvfp = 0;

  ub4 idnplen=0;

  // idents
  ub4 idcnt=0,id1cnt=0,id2cnt=0;

  // kwd, bltin, dunder
  ub4 kwcnt=0;
  ub4 bltcnt=0;
  ub4 duncnt=0;

  // str lits
  ub4 slitcnt=0;

  // flt lits
  ub4 flitcnt=0;

  nlitpos=0;
  flitpos=0;

  dentlvl=0;
  dentst[0] = 0;

  idsketch = 0;

  memset(id2chr,0,256);

  ub1 t=0,u;

  ub1 Q = 0;
  ub4 N = 0;
  ub1 R0 = 0;
  ub1 sign = 0;

  ub4 id=0;
  ub1 x1;
  ub4 x4;
  ub8 x8;

  ub4 hc=0;

  // nlit
  ub4 u4v=0;

  // flit
  ub4 fdp=0; // dot pos
  ub2 fxp=0; // e+nn
  bool fxs=0;

  // f-strings
  ub1 fstrco=0;
  ub1 fstrQ=0;

  enum Token kwd;
  enum token kw;
  ub4 idhilen = 0;

  memset(tkgrps,0,sizeof(tkgrps));
  memset(bolvlc,0,Depth);

  pass = 1;

  if (slen > 3 && sp[0] == '#' && sp[1] == '!') { // skip shebang
    n += 2;
    while(n < slen && sp[n] != '\n') n++;
  }

  infofln(FLN,"+lex1");

#undef  FLN
#define FLN __LINE__|(Shsrc_lex1<<16)

#include "lextb1.i"

#undef  FLN
#define FLN __LINE__|(msgfile<<16)

  infofln(FLN,"-lex1");

  if (dn == 0) {
    info("%s is empty",lsp->name);
    return 0;
  }

  tkcnt = dn;
  showcnt("x token",tkcnt);
  showcnt("line",l);
  lncnt = l;

  if (verbose) timeit2(&T1,slen,"pass 1 tokenised ` in");

  showcnt("2int n lit",ilit1cnt);
  showcnt("2int 1 lit",ilitcnt);
  showcnt("2flt lit",flitcnt);

  showcnt("2str n lit",slitcnt);
  showcnt("2str 0 lit",slit0cnt);
  showcnt("2str 1 lit",slit1cnt);
  showcnt("2str 2 lit",slit2cnt);
  showcnt("2str 3 lit",slit3cnt);
  showcnt("2str x lit",slitxcnt);

  if (slitcnt) mkslithash(slitcnt);

  slitcnt += slit1cnt + slit2cnt + slit3cnt + slitxcnt;

  if (slitcnt) {
    slitpos += 5 * (slitcnt+2); // align + 0-term
    showsiz("slit pool",slitpos);
    slitids = alloc(slitcnt,ub8,Mnofil,"lex slit ids",nextcnt);
  }
  slitcnt += slit0cnt;
  showcnt("2str n lit",slitcnt);

  if (idcnt) {
    showcnt("2ident / kwd",idcnt);
    if (idnplen >= Idpool) ice(n,"ID literal pool exceeds max %u`B",Idpool);
    info("idpool %u`B max len %u",idnplen,idhilen);
    idnplen += 5 * (idcnt+1); // align + 0-term
  }

  if (tkcnt >= Tokencnt) serror(n,"token count %u exceeds limit %u",tkcnt,Tokencnt);

  ub4 bitcnt = ilitcnt + flitcnt + slitcnt + slit2cnt + slit3cnt + slitxcnt;
  bitcnt += idcnt + id2cnt;

  ub4 xcnt = slit0cnt + slit1cnt + id1cnt + ilit1cnt;

  tkgrps[0] += bitcnt + xcnt;
  tkcnt += bitcnt + xcnt;

  showcnt("2ident2",id2cnt);
  showcnt("2ident1",id1cnt);

  showcnt("2token",tkcnt);

  if (tkcnt >= Tokencnt) serror(n,"token count %u exceeds limit %u",tkcnt,Tokencnt);

  uidcnt = exp_est();
  if (idcnt == 0) uidcnt = 0;

  showcnt("2est unique ident",uidcnt);

  showsiz("nlit pool",nlitpos);

  if (globs.rununtil == 2) { infofln(FLN,"until lex pass 1"); return 0; }

  tkpart[1].nel = tkpart[2].nel = tkpart[3].nel = tkcnt+Tkpad;
  tkpart[1].siz = 2;
  tkpart[2].siz = 1;
  tkpart[3].siz = 1;

  tkpart[0].siz = 8;
  tkpart[0].nel = xcnt;

  tkbas = allocset(tkpart,4,Mnofil,"lex tokens",nextcnt);
  bits = tkpart[0].ptr;
  dfps = tkpart[1].ptr;
  tks = tkpart[2].ptr;
  atrs = tkpart[3].ptr;

  if (tkpart[0].ismmap == 0) memset(atrs,0,tkcnt);

  nlittop = nlitpos; nlitpos = 0;
  if (nlittop) {
    nlitpool = alloc(nlittop,ub1,Mnofil,"lex nlit pool",nextcnt);
  }

  slittop = slitpos;
  if (slitpos) {
    slittop += 2;
    slitpool = alloc(slittop,ub1,Mnofil,"lex slit pool",nextcnt);
  }

  if (idnplen) idnampool = alloc(idnplen+4,ub1,Mnofil,"lex idnpool",nextcnt);
  idnampos = 4;

  if (idcnt) {
    mkidhash(uidcnt,idcnt);
    idtablen = uidcnt * 2 + 1024;
    idtab = alloc(idtablen,ub4,Mnofil,"lex id tab",nextcnt);
  }
  if (id2cnt) {
    mkid2tab();
  }
  memset(id1tab,0xff,256);

  slitpos = 4;

  lncnt += 2;
  lntab = lncnt < 1024 ? minalloc(lncnt * 4,4,Mnofil,"lex lntab") : medalloc(lncnt * 4,4,"lex lntab");

  setsrcmfile(mf,lntab,lncnt,n);

  n = 0;
  dn = bn = 0;
  l = 0;

  fxp = 0;
  fdp = 0;

  fstrco=0;
  fstrQ=0;

  dentlvl=0;
  dentst[0] = 0;

  idcnt = id1cnt = id2cnt = 0;

  pass = 2;

  infofln(FLN,"+lex2");

#undef  FLN
#define FLN __LINE__|(Shsrc_lex2<<16)

#include "lextb2.i"

#undef  FLN
#define FLN __LINE__|(msgfile<<16)

  infofln(FLN,"-lex2");

  if (verbose) timeit2(&T1,slen,"pass 2 tokenised `B in");

  showcnt("3token",tkcnt);
  showcnt("3line",l);

  showcnt("bltin",bltcnt);
  showcnt("dunder",duncnt);

  if (idhshcnt) {
    info("%3u` uniq idents max len %u",idhshcnt,idnmax);
    info("id hash table used %u` of %u` retry %u`",idhshcnt,idhshlen,idhshmis);
  }
  showcnt("3ident2",id2cnt);

  if (slithshcnt) {
    showcnt("3uniq slit",slithshcnt);
    showcnt("3uniq slit",slitucnt);
    info("slit hash table used %u` of %u`, skipped %u`",slithshcnt,slithshlen,slitlimcnt);
  }

  if (dn > tkcnt) {
    error("%u :1 vs %u :2 tokens dif %u",tkcnt,dn,dn - tkcnt);
    rv = 1;
  } else if (dn < tkcnt) {
    warning("%u :1 vs %u :2 tokens dif %u",tkcnt,dn,tkcnt - dn);
    rv = 0;
  }

  tkcnt = dn;

  lntab[l] = n;
  lntab[l+1] = n+1;

  memset(tks+tkcnt,T99_eof,Tkpad);

#if 0
  kwcnt = 0;
  for (dn = 0; dn < tkcnt; dn++) {
    tk = tks[dn];
    tkstats[tk]++;
  }
  for (tk = 0; tk < T99_count; tk++) {
    cnt = tkstats[tk];
    if (cnt) {
#if Kwcnt > 0
      if (tk < T99_kwd) kwcnt += cnt;
#endif
      info("%5u %s",cnt,tknam(tk));
    }
  }
  showcnt("kwd",kwcnt);
#endif

  for (grp = 0; grp < Tkgrp; grp++) {
    cnt = tkgrps[grp];
    if (cnt) info("grp %u %u",grp,cnt);
    if (grp < Tkgrps) lsp->tkgrps[grp] = cnt;
  }

  if (nlitpos == 0) { if (nlitpool) afree(nlitpool,"lex nlit pool",nextcnt); }
  else { lsp->nlitpool = nlitpool; lsp->nlittop = nlitpos; }
  lsp->nlitcnt = ilitcnt + flitcnt + ilit1cnt;

  if (slitxcnt) lsp->src = sp;

  showcnt("token",dn);

  lsp->tkcnt = dn;
  lsp->tbcnt = bn;

  lsp->toks = tks;
  lsp->bits = bits;
  lsp->atrs = atrs;
  lsp->dfps = dfps;

  lsp->idcnt = idcnt + id1cnt + id2cnt;
  lsp->uidcnt = uidcnt;
  lsp->uid1cnt = uid1cnt;
  lsp->uid2cnt = uid2cnt;

  lsp->idnampool = idnampool;

  lsp->slitpool = slitpool;
  lsp->slitcnt = slitcnt;
  lsp->slittop = slittop;

  lsp->tkbas = tkbas;

  memcpy(lsp->tkgrps,tkgrps,sizeof(tkgrps));

  if ( (globs.emit & 1) | (globs.log & 1) ) doemit(lsp,mf->name);

  if (bolvl) {
    sinfofln(hi32,bolvls[0],"opening '%c' here",bolvlc[0]);
    serror(n,"no matching '%c'",bolvlc[0]);
  }

  ub2 hidepth = (ub2)((ub1 *)memchr(bolvlc,0,Depth) - bolvlc);
  info("max depth %u",hidepth);
  lsp->hidepth = hidepth;

  if (idhsh) afree(idhsh,"lex id hash",nextcnt);

  if (globs.rununtil == 3) { infofln(FLN,"until lex"); return 0; }

  return rv;
}

int lexfile(ub4 fln,cchar *path,cchar *parpath,enum Inctype inc,struct lexsyn *lsp,ub8 T0)
{
  int rv;
  ub2 n = 0, dirsep = 0;
  char *ipath,*p;
  int fd = -1;
  struct fnaminf *fb;
  ub2 incdir=0;
  ub4 slen,xlen;
  ub4 nr;

  struct osstat ino;
  char chkpath[Pathname];

  ub8 T1=0;

  if (path == nil) { errorfln(FLN,fln,"nil file name for lex"); return 1; }
  else if (*path == 0) { errorfln(FLN,fln,"empty file name for lex"); return 1; }

  while (path[n]) { if (path[n] == '/') dirsep = n; n++; }
  if (n == dirsep + 1) { errorfln(FLN,fln,"lex: path '%s' is a directory",path); return 1; }

  timeit(&T1,nil);

  if (inc == Inone) {
    fd = tryopen(nil,path,1);
    if (fd == -1) return 1;
  } else if (inc == Iuser) { // search in dir of includer

    mysnprintf(chkpath,0,Pathname-2,"%.*s/%s",dirsep,parpath,path);
    fd = tryopen(nil,path,1);
    if (fd == -1) return 1;
  } else {

    while (fd == -1 && incdir < lsp->incdircnt) { // search in system dirs and -I
      mysnprintf(chkpath,0,Pathname-2,"%s/%s",lsp->incdirs[incdir],path);
      fd = tryopen(nil,chkpath,0);
      incdir++;
    }
    if (fd == -1) { errorfln(FLN,0,"cannot open %s",path); return hi32; }
  }

  if (osfdinfo(&ino,fd)) { osclose(fd); errorfln(FLN,0,"cannot get info for %s",path); return 1; }
//  if (ino.len >= (1U << 31)) { osclose(fd); errorfln(FLN,0,"%s length %lu exceeds %u",path,ino.len,1U << 31); return 1; }

  fb = getsrcmfile();

  fb->ino = ino.ino;
  fb->dev = ino.dev;

  if (chksrcmfile(fb)) {
    info("skip duplicate %s ",path);
    osclose(fd);
    return 0;
  }

  srcnam = path;

  slen = (ub4)ino.len;
  xlen = slen + max(Cclen+2,8); // pad with 0

  ub1 *sp;

  if (slen == 0) { info("%s is empty",path); osclose(fd); return 0; }
  sp = alloc(xlen,char,Mnofil,"lex file",nextcnt);
  memset(sp+slen,0,xlen-slen);

  memset(sp,0,slen);

  timeit(&T1,nil);

  rv = osread(fd,sp,slen,&nr);
  if (rv) {
    if (rv < 0) oserror("cannot read %s",path);
    else error("cannot read %s",path);
    osclose(fd);
    return 1;
  }
  osclose(fd);
//  if (nr != slen) { error("partial read %'uB of %'uB of %s",(ub4)nr,(ub4)slen,path); return 1; }

  ipath = minalloc(n+1,1,Mnofil,"lex inc path");
  memcpy(ipath,path,n);
  ipath[n] = 0;

  p = ipath + n;
  while (p > ipath && p[-1] != '/') p--;
  fb->name = p;
  fb->path = ipath;
  fb->dirsep = dirsep;
  fb->len = slen;
  fb->incdir = incdir;

  if (verbose) timeit2(&T1,slen,"read ` in");
  msgfls();

  srcnam = path + dirsep;

  lsp->srclen = slen;

  if (globs.rununtil == 1) { infofln(FLN,"until file"); return 0; }

  rv = lex(fb,sp,slen,lsp,T1);

  if (lsp->src == nil) afree(sp,"src",nextcnt);
  msgfls();

  if (rv || lsp->tkcnt == 0) return rv;

  timeit2(&T0,slen,"tokenised ` in");

  return 0;
}

int lexstr(const unsigned char * restrict str,ub2 slen,struct lexsyn *lsp,ub8 T0)
{
  ub8 T1 = T0;
  struct fnaminf *fb;
  int rv;

  fb = getsrcmfile();
  fb->path = "(cmdline)";

  fb->len = slen;
  rv = lex(fb,str,slen,lsp,T1);

  if (rv) return rv;

  timeit2(&T0,slen,"tokenised ` in");
  return 0;
}

