static int tryopen(cchar *dir,cchar *name,bool need)
{
  char path[Pathname];
  cchar *p;
  int fd;

  if (dir == nil || *dir == 0 || (dir[0] == '.' && dir[1] == '/') ) p = name;
  else {
    mysnprintf(path,0,Pathname,"%s/%s",dir,name);
    p = path;
  }

  if (osopenseq(p,&fd) && need) oserror("cannot open %s",p);
  return fd;
}

