static void mkidhash(ub4 estcnt,ub4 cnt)
{
  ub4 hlen,fac;
  ub4 bit = 0;

  idhshcnt = 0;

  if (estcnt > hi20) {
    warning("estimated unique ID count %u`",estcnt);
    estcnt = min(estcnt,hi24);
    fac = 4;
  } else if (estcnt > hi16) fac = 8;
  else fac = 16;
  hlen = estcnt * fac;

  idhshlen = 1;
  while (idhshlen < hlen) { idhshlen <<= 1; bit++; }
  idhshmask = idhshlen-1;
  idhshbit = bit;

  hlen = idhshlen;
  idhsh = alloc(hlen,ub4,0,"lex id hash",nextcnt);
  info("%u entry id hash from u/id %u/%u",idhshlen,estcnt,cnt);
}

static ub4 id_nplen;

static ub4 idcheck(const ub1 *nam,ub2 len,ub4 v)
{
  ub4 x = idhsh[v];
  ub4 np,id;

  if (x == 0) { // slot free
    id = uidcnt++;
    if (id >= idtablen) ice(hi32,"id %u '%.*s' exceeds %u",id,len,nam,idtablen);
    idhsh[v] = id;
//    vrb("add '%s'",idnampool+idnampos);
    np = idnampos = align4(idnampos);
    if (np + len + 1 >= id_nplen) ice(hi32,"id nampool %u '%.*s' exceeds %u",np+len,len,nam,id_nplen);
    memcpy(idnampool+np,nam,len);
    idhshcnt++;
    if (len > idnmax) idnmax = len;
    idtab[id] = np;
    // info("add id %u nid %u",id,np);
    idnampos = np + len + 1;
    return id; // new
  } else {
    np = idtab[x];
    if (np + len + 1 >= id_nplen) ice(hi32,"id nampool %u '%.*s' exceeds %u",np+len,len,nam,id_nplen);
    if (idnampool[np+len] == 0 && samestr(idnampool+np,nam,len)) return x; // existing: common
    else {
      idhshmis++;
      return hi32;
    }
  }
}

// get or if none insert id. returns idnid.idid
static ub4 idgetadd(const ub1 *nam,ub2 len,ub4 hc)
{
  ub4 v,v0;
  ub4 x;
  ub4 hc2;

  // info("add id %.*s",len,nam);
  hc = (hc >> idhshbit) ^ hc;
  v = hc & idhshmask;

  x = idcheck(nam,len,v);
  if (x != hi32) return x;
  else { // probe once with second hash
    hc2 = hc >> idhshbit;
    v += hc2 & idhshmask;
    if (v >= idhshlen) v -= idhshlen;
    x = idcheck(nam,len,v);
    if (x != hi32) return x;
    else {
      v0 = v;
      do { // linear
        v = (v + 1) & idhshmask;
        x = idcheck(nam,len,v);
        if (x != hi32) return x;
      } while (v != v0);
      ice(0,"TODO resize id hash table overflow at %u of %u",idhshcnt,idhshlen);
    }
  }
}

