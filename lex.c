static void mkid2tab(void) {
  ub2 c;
  ub2 n1=0,n2=0;
  ub1 x;

  for (c = 0; c < 256; c++) {
    id2ch1map[c] = 0xff;
    id2ch2map[c] = 0xff;
    x = id2chr[c];
    if (x & 1) { id2ch1map[c] = n1; id2ch1imp[n1++] = c; }
    if (x & 2) { id2ch2map[c] = n2; id2ch2imp[n2++] = c; }
  }
  vrb("id2map %u,%u",n1,n2);
  if ( (n1 | n2) == 0) return;
  id2nch1 = nxpwr2(n1,&id2shift1);
  id2nch2 = nxpwr2(n2,&id2shift2);
  id2mask1 = id2nch1-1;
  id2mask2 = id2nch2-1;
  id2tab = (ub2 *)minalloc(id2nch1 * id2nch2 * 2,2,0xff,"lex id2tab");
  id2nams = (ub2 *)minalloc(id2nch1 * id2nch2 * 2,2,Mnofil,"lex id2nam");
}

static void mkslithash(ub4 cnt)
{
  ub4 hlen;
  ub4 bit = 0;

  if (cnt < 16) hlen = cnt+8;
  else if (cnt > hi20) {
    warning("string lit count %u`",cnt);
    cnt = hi20;
    hlen = cnt * 4;
  } else hlen = cnt * 8;

  slithshlen = 1;
  while (slithshlen < hlen) { slithshlen <<= 1; bit++; }
  slithshmask = slithshlen-1;
  slithshbit = bit;

  slithsh = alloc(slithshlen,ub4,0,"lex slit hash",nextcnt);
  info("%u entry slit hash",slithshlen);
}

static ub4 slitcheck(ub1 *pool,ub4 nam0,ub4 nam1,ub4 v)
{
  ub4 x,np,id;
  ub4 l1,len = nam1 - nam0;

  if (v >= slithshlen) ice(0,"slit ndx %u above %u for %.*s",v,slithshlen,len,pool+nam0);

  x = slithsh[v];

  if (x == 0) { // slot free - consolidate pool
    id = slitucnt++;
    slithsh[v] = id;
    np = slitpos;
    // info("v %u x %u nam0 %u top %u",v,x,nam0,slittop);
    slitpos = align4(nam1 + 1);
    slithshcnt++;
    slitids[id] = np | ((ub8)len << 32);
    vrb("add slit len %u '%.*s' at %u",len,min(len,80),pool+nam0,nam0);
    return id; // new

  } else {
    l1 = slitids[x] >> 32;
    np = slitids[x] & hi32;
    // info("v %u x %u nam0 %u top %u",v,x,nam0,slittop);
    if (l1 == len && memcmp(pool + np,pool + nam0,len) == 0) return x;
    else return 0;
  }
}

// get or if none insert
static ub4 slitgetadd(ub1 *pool,ub4 nam1)
{
  ub4 nam0 = slitpos;
  ub4 len = nam1 - nam0;
  ub4 v;
  ub4 x,id;
  ub4 hc,hc2;
  ub2 lim;

  pool[nam1] = 0;

  if (len >= Slitint) {
    slitpos = align4(nam1 + 1);
    id = slitucnt++;
    slitids[id] = nam0 | ((ub8)len << 32);
    return id;
  }

  hc = hashalstr(pool+nam0,len,Hshseed);

  hc = (hc >> slithshbit) ^ hc;
  v = hc & slithshmask;

  x = slitcheck(pool,nam0,nam1,v);
  if (x) return x;
  else { // probe once with second hash
    hc2 = hc >> slithshbit;
    v += hc2 & slithshmask;
    if (v >= slithshlen) v -= slithshlen;
    x = slitcheck(pool,nam0,nam1,v);
    if (x) return x;
    else {
      lim=4;
      do { // linear
        v = (v + 1) & slithshmask;
        x = slitcheck(pool,nam0,nam1,v);
        if (x) return x;
      } while (--lim);
      slitpos = align4(nam1 + 1); // skip dedup
      id = slitucnt++;
      slitids[id] = nam0 | ((ub8)len << 32);
      return id;
    }
  }
}

