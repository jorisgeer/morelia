{
  ub1 x1,x2,c = src[sn],d = src[sn+1];

  x1 = lxatox1(c);
  x2 = lxatox1(d);

  if ( (x1 | x2) > 0xf) serror(0,"illegal hex escape seq '%s%s'",chprint(c),chprint(d));
  return x1 << 4 | x2;
}

