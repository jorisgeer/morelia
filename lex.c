static ub1 doesco(const ub1 * restrict src,ub4 sn)
{
  ub1 nc,x1,c = src[sn];

  x1 = c - '0';
  nc = src[sn+1]; if (nc >= '0' && nc < '8') x1 = x1 << 3 | (nc - '0');
  nc = src[sn+2]; if (nc >= '0' && nc < '8') x1 = x1 << 3 | (nc - '0');
  return x1;
}

