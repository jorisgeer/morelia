static inline bool samestr1(const ub1 *p,ub4 a,ub4 b,ub2 n)
{
  ub4 n4 = n >> 2;
  const ub4 *pa = (const ub4 *)(p + a);
  const ub4 *pb = (const ub4 *)(p + b);

  if (n4) {
    p += (n4 << 2);
    do {
      if (*pa != *pb) return 0;
      pa++; pb++;
    } while (--n4);
  }
  switch (n & 3) {
  case 0: return 1;
  case 1: return p[a] == p[b];
  case 2: return p[a] == p[b] && p[a+1] == p[b+1];
  case 3: return p[a] == p[b] && p[a+1] == p[b+1]  && p[a+2] == p[b+2];
  }
  return 0;
}

static inline bool samestr(const ub1 *a,const ub1 *b,ub2 n)
{
  ub2 i,n4 = n >> 2;
  const ub4 *a4 = (const ub4 *)a;
  const ub4 *b4 = (const ub4 *)b;

  if (n4) {
    i = 0;
    do {
      if (a4[i] != b4[i]) return 0;
    } while (++i < n4);
  }
  i = (n4 << 2);
  switch (n & 3) {
  case 0: return 1;
  case 1: return a[i] == b[i];
  case 2: return a[i] == b[i] && a[i+1] == b[i+1];
  case 3: return a[i] == b[i] && a[i+1] == b[i+1]  && a[i+2] == b[i+2];
  }
  return 0;
}

