static void mkidhash(ub4 estcnt,ub4 cnt)
{
  ub4 hlen;
  ub4 bit = 0;

  idhshcnt = 0;

  if (estcnt > cnt) estcnt = cnt;
  else if (estcnt < 128) estcnt = 128;
  if (cnt < 16) hlen = cnt+4;
  else if (estcnt > hi24) {
    warning("estimated unique ID count %u`",estcnt);
    estcnt = min(estcnt,hi28);
    hlen = estcnt * 4;
  } else hlen = estcnt * 16;

  idhshlen = 1;
  while (idhshlen < hlen) { idhshlen <<= 1; bit++; }
  idhshmask = idhshlen-1;
  idhshbit = bit;

  hlen = idhshlen;
  idhsh = alloc(hlen,ub4,0,"lex id hash",nextcnt);
  info("%u entry id hash from u/id %u/%u",idhshlen,estcnt,cnt);
}

static ub4 idcheck(ub4 nam,ub2 len,ub4 v)
{
  ub4 x = idhsh[v];
  ub4 np,id;

  if (x == 0) { // slot free
    id = uidcnt++;
    if (id >= idtablen) ice(hi32,"id '%.*s' exceeds %u",len,idnampool+nam,id);
    idhsh[v] = id;
    np = idnampos;
//    vrb("add '%s'",idnampool+idnampos);
    idnampos = align4(nam + 1);
    idhshcnt++;
    if (len > idnmax) idnmax = len;
    idtab[id] = np;
    info("add id %u nid %u",id,np);
    return id; // new
  } else {
    np = idtab[x];
    if (samestr1(idnampool,np,idnampos,len)) return x; // existing: common
    else {
      idhshmis++;
      return hi32;
    }
  }
}

// get or if none insert id. returns idnid.idid
static ub4 idgetadd(ub4 nam,ub4 hc)
{
  ub4 v,v0;
  ub4 x;
  ub4 hc2;
  ub2 len = nam - idnampos;

  hc = (hc >> idhshbit) ^ hc;
  v = hc & idhshmask;

  idnampool[nam] = 0;

  x = idcheck(nam,len,v);
  if (x != hi32) return x;
  else { // probe once with second hash
    hc2 = hc >> idhshbit;
    v += hc2 & idhshmask;
    if (v >= idhshlen) v -= idhshlen;
    x = idcheck(nam,len,v);
    if (x != hi32) return x;
    else {
      v0 = v;
      do { // linear
        v = (v + 1) & idhshmask;
        x = idcheck(nam,len,v);
        if (x != hi32) return x;
      } while (v != v0);
      ice(0,"TODO resize id hash table overflow at %u of %u",idhshcnt,idhshlen);
    }
  }
}

